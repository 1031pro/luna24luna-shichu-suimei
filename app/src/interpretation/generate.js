function strongestElement(balance) {
  return Object.entries(balance).sort((a, b) => b[1] - a[1])[0]?.[0] || "木";
}

function weakestElement(balance) {
  return Object.entries(balance).sort((a, b) => a[1] - b[1])[0]?.[0] || "木";
}

